#Read 2 numbers to variable a and b and perform all bitwise operations on that numbers.

num1 = int(input("Enter First Number: "))
num2 = int(input("Enter Second Number: "))

print("\nbitwise AND operation num1 & num2 =", num1 & num2)

print("\nbitwise OR operation num1 | num2 =", num1 | num2)
 
print("\nbitwise NOT operation ~num1 =", ~num1)
 
print("\nbitwise XOR operation num1 ^ num2 =", num1 ^ num2)

print("\nbitwise right shift operator num1 >> 1 =", num1 >> 1)
print("\nbitwise right shift operator num2 >> 1 =", num2 >> 1)

print("\nbitwise left shift operator num1 << 1 =", num1 << 1)
print("\nbitwise left shift operator  num2 << 1 =", num2 << 1)
